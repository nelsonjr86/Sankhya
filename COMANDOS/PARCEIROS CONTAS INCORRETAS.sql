SELECT C.CODPARC, P.TIPOGERBOLETO , P.TIPGERBOLCENT, C.SUGTIPNEGSAID
FROM TGFPAR P
INNER JOIN TGFCPL C ON (P.CODPARC=C.CODPARC)
WHERE SUGTIPNEGSAID IN (100, 200,300,400,600,700,900,800, 1100,1200 )
AND MOTBLOQ NOT LIKE '%RESUM%'
AND ATIVO = 'S'
AND P.TIPPESSOA ='J'

