FROM TMDFMG FMG INNER JOIN TGFPAR PAR ON (FMG.EMAIL=PAR.EMAIL)
WHERE FMG.DTENTRADA BETWEEN '28/12/2019' AND '01/01/2020'
AND FMG.ASSUNTO LIKE '%Real onibus - Comunicado%'
and FMG.STATUS LIKE 'Erro: Não Enviada               '
