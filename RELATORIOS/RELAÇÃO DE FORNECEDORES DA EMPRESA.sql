SELECT DISTINCT PAR.RAZAOSOCIAL, PAR.CGC_CPF AS CNPJ
FROM TGFPAR PAR INNER JOIN TGFCAB CAB ON (PAR.CODPARC = CAB.CODPARC)
INNER JOIN TGFITE ITE ON (ITE.NUNOTA=CAB.NUNOTA)
INNER JOIN TGFPRO PRO ON (PRO.CODPROD=ITE.CODPROD)
WHERE PAR.ATIVO = 'S'
AND PAR.FORNECEDOR ='S'
AND PAR.TRANSPORTADORA = 'N'
AND PAR.TIPPESSOA = 'J'
AND ITE.USOPROD = 'R'
AND PRO.USOPROD = 'R'
AND PAR.CLASSIFICMS = 'R'
AND CAB.TIPMOV = 'C'
AND CAB.DTNEG >= '01/01/2018'
AND RAZAOSOCIAL NOT LIKE '%CLARO%'
AND RAZAOSOCIAL NOT LIKE '%REAL ONIBUS%'
AND RAZAOSOCIAL NOT LIKE '%TELEMAR%'
AND RAZAOSOCIAL NOT LIKE '%TELEFONICA%'
AND RAZAOSOCIAL NOT LIKE '%TIM%'
AND RAZAOSOCIAL NOT LIKE '%CEMIG%'
AND RAZAOSOCIAL NOT LIKE '%ATACADAO%'
AND RAZAOSOCIAL NOT LIKE '%APOIO MINEIRO%'
AND RAZAOSOCIAL NOT LIKE '%LOJA ELETRICA%'
AND RAZAOSOCIAL NOT LIKE '%COMERCIAL AGRO FORTE EIRELI%'
AND RAZAOSOCIAL NOT LIKE '%ARMAZEM CORAL LTDA%'
AND RAZAOSOCIAL NOT LIKE '%PRATI MATERIAL DE CONSTRUCAO%'
AND RAZAOSOCIAL NOT LIKE '%CROI COMPUTADORES LTDA%'
AND RAZAOSOCIAL NOT LIKE '%CENTRAL VIDROS AUTOMOTIVOS%'
AND RAZAOSOCIAL NOT LIKE '%COMERCIAL FERREIRA E BASTOS%'
AND RAZAOSOCIAL NOT LIKE '%COMERCIO DE ACOS BRASILIAINOX%'
AND RAZAOSOCIAL NOT LIKE '%COMERCIO DE AÇOS JACINOX%'
AND RAZAOSOCIAL NOT LIKE '%J PLACIDO MATERAIS PARA CONSTRUÇÃO%'
AND RAZAOSOCIAL NOT LIKE '%LIDIS COMERCIO E REPRESENTACOES%'
AND RAZAOSOCIAL NOT LIKE '%MADEIRAS TERRA NOVA COMERCIO%'
AND RAZAOSOCIAL NOT LIKE '%SHN LATAS E ACESSORIOS PARA VEICULOS%'
AND RAZAOSOCIAL NOT LIKE '%STOP BUS - RECIFE %'
AND RAZAOSOCIAL NOT LIKE '%SUPER ACO LTDA%'
AND RAZAOSOCIAL NOT LIKE '%POLOBUS COMERCIO%'

ORDER BY PAR.RAZAOSOCIAL

